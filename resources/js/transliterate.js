function transliterate(text) {
    const translitMap = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
        'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
        'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
        'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
        'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch',
        'ш': 'sh', 'щ': 'shch', 'ъ': '', 'ы': 'y', 'ь': '',
        'э': 'e', 'ю': 'yu', 'я': 'ya'
    };

    return text
        .toLowerCase() // Приводим к нижнему регистру
        .split('') // Разбиваем строку на массив символов
        .map(char => translitMap[char] || char) // Заменяем символы
        .join('') // Собираем строку обратно
        .replace(/[^a-z0-9-]/g, '-') // Убираем лишние символы
        .replace(/--+/g, '-') // Заменяем несколько дефисов на один
        .replace(/^-|-$/g, ''); // Убираем дефисы в начале и конце
}

export default transliterate;
